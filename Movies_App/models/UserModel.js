const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    max: 50,
  },
  likedMovies: Array,
});

module.exports = mongoose.model("users", userSchema);
//userSchema is an instance of the mongoose.Schema class,
//which allows you to define the structure and validation rules for your data.
